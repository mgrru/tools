local opt = vim.opt

-- 行号
opt.number = true

-- 缩进
opt.tabstop = 2
opt.shiftwidth = 2
opt.expandtab = true
opt.autoindent = true

-- 光标
opt.cursorline = true

-- 防止包裹
opt.wrap = false

-- 新建窗口位置
opt.splitright = true
opt.splitbelow = true

-- 搜索
opt.smartcase = true

-- 外观
opt.termguicolors = true
opt.signcolumn = "yes"
